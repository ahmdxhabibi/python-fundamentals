# Tipe Data

# Tipe data integer (bilangan bulat)
angka_bulat = 10
print("Tipe data angka_bulat:", type(angka_bulat))  # Output: <class 'int'>

# Tipe data float (bilangan desimal)
angka_desimal = 10.5
print("Tipe data angka_desimal:", type(
    angka_desimal))  # Output: <class 'float'>

# Tipe data string (teks)
teks = "Halo, dunia!"
print("Tipe data teks:", type(teks))  # Output: <class 'str'>

# Tipe data boolean (benar/salah)
benar = True
salah = False
print("Tipe data benar:", type(benar))  # Output: <class 'bool'>
print("Tipe data salah:", type(salah))  # Output: <class 'bool'>

# Tipe data list (daftar)
daftar = [1, 2, 3, 4, 5]
print("Tipe data daftar:", type(daftar))  # Output: <class 'list'>

# Tipe data tuple (daftar tetap)
daftar_tetap = (1, 2, 3, 4, 5)
print("Tipe data daftar_tetap:", type(daftar_tetap))  # Output: <class 'tuple'>

# Tipe data dictionary (kamus)
kamus = {"nama": "Andi", "usia": 25, "tinggi": 175.5}
print("Tipe data kamus:", type(kamus))  # Output: <class 'dict'>

# Tipe data set (himpunan)
himpunan = {1, 2, 3, 4, 5}
print("Tipe data himpunan:", type(himpunan))  # Output: <class 'set'>

# Menampilkan nilai-nilai dari masing-masing variabel
print("\nNilai dari masing-masing variabel:")
print("angka_bulat:", angka_bulat)
print("angka_desimal:", angka_desimal)
print("teks:", teks)
print("benar:", benar)
print("salah:", salah)
print("daftar:", daftar)
print("daftar_tetap:", daftar_tetap)
print("kamus:", kamus)
print("himpunan:", himpunan)
