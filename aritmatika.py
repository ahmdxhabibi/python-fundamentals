# Operasi Aritmatika dalam Python

# Deklarasi variabel
a = 10
b = 3

# Penjumlahan
hasil_penjumlahan = a + b
# Output: Penjumlahan: 10 + 3 = 13
print("Penjumlahan:", a, "+", b, "=", hasil_penjumlahan)

# Pengurangan
hasil_pengurangan = a - b
# Output: Pengurangan: 10 - 3 = 7
print("Pengurangan:", a, "-", b, "=", hasil_pengurangan)

# Perkalian
hasil_perkalian = a * b
# Output: Perkalian: 10 * 3 = 30
print("Perkalian:", a, "*", b, "=", hasil_perkalian)

# Pembagian
hasil_pembagian = a / b
# Output: Pembagian: 10 / 3 = 3.3333333333333335
print("Pembagian:", a, "/", b, "=", hasil_pembagian)

# Pembagian integer (floor division)
hasil_floor_division = a // b
# Output: Pembagian Integer: 10 // 3 = 3
print("Pembagian Integer:", a, "//", b, "=", hasil_floor_division)

# Modulus (sisa bagi)
hasil_modulus = a % b
print("Modulus:", a, "%", b, "=", hasil_modulus)  # Output: Modulus: 10 % 3 = 1

# Perpangkatan
hasil_perpangkatan = a ** b
# Output: Perpangkatan: 10 ** 3 = 1000
print("Perpangkatan:", a, "**", b, "=", hasil_perpangkatan)
