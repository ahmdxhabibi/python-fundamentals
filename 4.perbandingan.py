# Operator Perbandingan

# Deklarasi variabel
a = 10
b = 3

# Sama dengan (==)
print("Apakah a sama dengan b?")
print(a == b)  # Output: False

# Tidak sama dengan (!=)
print("Apakah a tidak sama dengan b?")
print(a != b)  # Output: True

# Lebih besar dari (>)
print("Apakah a lebih besar dari b?")
print(a > b)  # Output: True

# Lebih kecil dari (<)
print("Apakah a lebih kecil dari b?")
print(a < b)  # Output: False

# Lebih besar atau sama dengan (>=)
print("Apakah a lebih besar atau sama dengan b?")
print(a >= b)  # Output: True

# Lebih kecil atau sama dengan (<=)
print("Apakah a lebih kecil atau sama dengan b?")
print(a <= b)  # Output: False


# Operator Logika

# Deklarasi variabel
a = 10
b = 3

# dan (and)
print("Apakah a lebih besar dari b DAN a lebih kecil dari 20?")
print(a > b and a < 20)  # Output: True

# atau (or)
print("Apakah a lebih besar dari b ATAU a lebih besar dari 20?")
print(a > b or a > 20)  # Output: True

# bukan (not)
print("Apakah TIDAK BENAR bahwa a lebih kecil dari b?")
print(not a < b)  # Output: True
